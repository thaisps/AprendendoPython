# Faça um programa que leia 5 números e informe o maior número.

print("Olá! Você irá inserir um total de 5 números!")
n1 = float(input("Insira um número."))
n2 = float(input("Insira mais um número."))
n3 = float(input("Insira mais um número."))
n4 = float(input("Insira mais um número."))
n5 = float(input("Insira mais um número."))
print("O número "+ str(max(n1,n2,n3,n4,n5)) + " é o maior entre o 5 números inseridos!")